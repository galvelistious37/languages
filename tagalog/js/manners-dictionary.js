
function getTagalogManners(){
    let dictionary = new Map();
    dictionary.set("Pakiusap", "Please");
    dictionary.set("Paki-(verb)", "Please (verb)");
    dictionary.set("[Maraming] Salamat", "Thank you [very much]");
    dictionary.set("Walang anuman", "You're welcome");
    dictionary.set("Ingat", "Take care");
    dictionary.set("Opo", "Yes");
    dictionary.set("Po (appears in the middle or end of answer)", "Po? = Yeah?, Come again?");
    dictionary.set("Hindi po", "No");
    dictionary.set("Mano po", "[your] hand please");
    dictionary.set("Magandang umaga po", "Good morning (polite)");
    dictionary.set("Kumusta po kayo", "How are you all? (polite, formal)");
    dictionary.set("Ate", "Older sister");
    dictionary.set("Kuya", "Older brother");
    dictionary.set("Makikiraan po", "Excuse me (polite)");
    dictionary.set("Mawalang galang po", "Excuse me (interrupt to ask a question)");
    dictionary.set("Paumanhin (po)", "I'm sorry (even more polite)");
    dictionary.set("Patawarin mo ako", "Please forgive me (casual)");
    dictionary.set("Patawarin niyo po ako", "Please forgive me (formal)");
    dictionary.set("Pasensiya na", "I'm sorry (casual)");
    dictionary.set("Pasensiya na po kayo", "I'm sorry (formal)");
    dictionary.set("Excuse me po and I'm sorry po", "English words with po attached to sound more polite");
    return dictionary;
}

